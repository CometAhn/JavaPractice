
public class example2 {

  public static void main(String[] args) {

    int i = 0;
    System.out.println("첫번쩨 i = " + i);
    System.out.println("두번째 i = " + i++);
    System.out.println("서번째 i = " + i);

    i = 0;
    System.out.println("네번째 i = " + ++i);
    System.out.println("다셋번째 i = " + i);

    int j = 10;
    System.out.println("첫번쩨 j = " + j);
    System.out.println("두번쩨 j = " + j--);
    System.out.println("세번쩨 j = " + j);


  }

}
